# update the year  of the car by using  the update () method :

thisdict = {
    "brand ":"Ford" ,
    "model":"Mustang" ,
    "year": 1964
}
thisdict.update({"year": 2020})
print(thisdict)

# also add  a value using update method

thisdict = {
    "brand ":"Ford" ,
    "model":"Mustang" ,
    "year": 1964
}
thisdict.update({"color " : "red"})
print(thisdict)